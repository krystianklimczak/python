def make_shirt(size, text):
    """Wyświetla informoacje dotyczące zamówionej koszulki."""
    print(f"\nKoszulka którą zamówiłeś będzie rozmiaru: '{size}'.")
    print(f"Tekst, który znajdzie się na koszulce to: '{text}'.")


make_shirt("L", "Giga Czad")
